// Addition Operation 
var shirtPrice = 500;
var pantPrice = 600;
var totalPrice = shirtPrice + pantPrice;

console.log(totalPrice);

// Subtraction Operation 
var laptopPrice = 70000;
var phonePrice = 27000;
var sub = laptopPrice - phonePrice;

console.log(sub);

// Multiplication Operation 
var fatherAge = 50;
var myAge = 20;
var multi = fatherAge * myAge;

console.log(multi);

// Division Operation 
var iceCreamPrice = 50;
var chipsPrice = 10;
var div = iceCreamPrice / chipsPrice;

console.log(div);

// Modulus Operation 
var num1 = 20;
var num2 = 6;
var mod = num1 % num2;

console.log(mod);
