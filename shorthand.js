// Addition Operation Shorthand
var price = 35;
// price = price + 10;
price += 10; // price = price + 10;
console.log(price);

// Subtraction Operation Shorthand
var fridge = 48000;
// fridge = fridge - 1000; 
fridge -= 1000; // fridge = fridge - 1000
console.log(fridge);

// Multiplication Operation Shorthand
var orangePrice = 180;
// orangePrice = orangePrice * 3;
orangePrice *= 3; // orangePrice = orangePrice * 3;
console.log(orangePrice);

// Division Operation Shorthand
var applePrice = 160;
// applePrice = applePrice / 3;
applePrice /= 10; // applePrice = applePrice / 3;
console.log(applePrice);

// Modulus Operation Shorthand
var number = 20;
// number = number % 6;
number %= 6; // number = number % 6;
console.log(number);